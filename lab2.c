#include<stdio.h>
#include<time.h>

 int main()
  {
  int x, sec, min, hour;
  
  x=time(NULL); 
  x=x+3*3600;              //Учёт часового пояса
  sec=x%60;                //Вычисление текучего количества секунд
  min=(x%3600)/60;         //Вычисление текущего количества минут
  hour=(x%(3600*24))/3600; //Вычисление текущего количества часов
  
  printf("Текущее время: %02d:%02d:%02d\n",hour,min,sec);
  return 0;
  }
